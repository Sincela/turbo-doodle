package com.company;

public class Main {

    public static void main(String[] args) {
        int[][] a = new int[10][10]; {
        }
        for (int i = 0; i < a.length; i++) {
            for (int j = 0; j < a[i].length; j++) {
                System.out.print(a[i][j] + 10 + "\t");
            }
            System.out.println();
        }
    }
}


