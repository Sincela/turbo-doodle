package com.company;

public class matrix2 {
    public static void main(String[] args) {

        int[][] matrix2 = new int[10][10]; {
        }

        for (int i = 0; i < matrix2.length; i++) {
            for (int j = 0; j < matrix2[i].length; j++) {
                System.out.print(matrix2[i][j]+10 + "\t");
            }
            System.out.println();
        }
    }
}
